import { z } from "zod";

const nullableScore = z.number().min(0).max(1).nullable().optional();
const optionalString = z.string().nullable().optional();

export const evaluationConfidenceBreakdownSchema = z.object({
  answerability_score: nullableScore,
  top_retrieval_score: nullableScore,
  average_retrieval_score: nullableScore,
  retrieval_margin: nullableScore,
  lexical_coverage: nullableScore,
  top_chunk_lexical_coverage: nullableScore,
  numeric_consistency: nullableScore,
  numeric_mismatch: z.boolean().default(false),
  scope_risk: z.boolean().default(false),
  direct_support: z.boolean().nullable().optional(),
  query_numeric_claims: z.array(z.string()).default([]),
  evidence_numeric_claims: z.array(z.string()).default([]),
  missing_numeric_claims: z.array(z.string()).default([]),
  matched_query_terms: z.array(z.string()).default([]),
  missing_query_terms: z.array(z.string()).default([]),
  decision_reasons: z.array(z.string()).default([]),
  scope_risk_reason: optionalString,
});

export const evaluationCaseSchema = z.object({
  id: z.string().min(1),
  question: z.string().min(1),
  category: z.string().min(1),
  difficulty: z.string().min(1),
  evaluation_focus: z.array(z.string()).default([]),
  should_answer: z.boolean(),
  answer_ready: z.boolean(),
  readiness_correct: z.boolean(),
  evidence_status: z.string().min(1),
  confidence_score: z.number().min(0).max(1),
  confidence_breakdown: evaluationConfidenceBreakdownSchema.nullable().optional(),
  answerability_score: nullableScore,
  top_retrieval_score: nullableScore,
  average_retrieval_score: nullableScore,
  retrieval_margin: nullableScore,
  lexical_coverage: nullableScore,
  top_chunk_lexical_coverage: nullableScore,
  numeric_consistency: nullableScore,
  numeric_mismatch: z.boolean().default(false),
  scope_risk: z.boolean().default(false),
  direct_support: z.boolean().nullable().optional(),
  decision_reasons: z.array(z.string()).default([]),
  expected_pages: z.array(z.number().int().positive()).default([]),
  retrieved_pages: z.array(z.number().int().positive()).default([]),
  page_hit: z.boolean().nullable().optional(),
  expected_answer_keywords: z.array(z.string()).default([]),
  matched_keywords: z.array(z.string()).default([]),
  missing_keywords: z.array(z.string()).default([]),
  keyword_match_score: nullableScore,
  answer: z.string(),
  fallback_used: z.boolean(),
  fallback_correct: z.boolean().nullable().optional(),
  citation_count: z.number().int().min(0),
  retrieved_filenames: z.array(z.string()).default([]),
  citation_scores: z.array(z.number().min(0)).default([]),
  top_citation_score: z.number().min(0).nullable().optional(),
  average_citation_score: z.number().min(0).nullable().optional(),
  duplicate_citation_count: z.number().int().min(0).default(0),
  latency_ms: z.number().min(0),
  llm_provider: z.string().min(1),
  model_name: optionalString,
  case_passed: z.boolean(),
  error_type: z.string().max(120).nullable().optional(),
  provider_fallback_used: z.boolean().nullable().optional(),
  generation_attempt_count: z.number().int().positive().nullable().optional(),
  generation_error_type: z.string().max(120).nullable().optional(),
});

export const evaluationArtifactSchema = z.object({
  artifact: z.object({
    updated_at: z.string().min(1),
    question_count: z.number().int().min(0),
    benchmark_question_count: z.number().int().min(0).nullable().optional(),
    is_partial: z.boolean(),
  }),
  run: z.object({
    run_id: z.string().min(1),
    started_at_utc: optionalString,
    completed_at_utc: optionalString,
    duration_ms: z.number().min(0).nullable().optional(),
    backend_base_label: z.string().min(1),
    endpoint: optionalString,
    dataset_path: optionalString,
    dataset_sha256: optionalString,
    top_k: z.number().int().positive().nullable().optional(),
    timeout_seconds: z.number().min(0).nullable().optional(),
    request_delay_seconds: z.number().min(0).nullable().optional(),
    question_count: z.number().int().min(0),
    backend_health: z
      .object({
        latency_ms: z.number().min(0).nullable().optional(),
        status: optionalString,
      })
      .nullable()
      .optional(),
    duplicate_citation_warning: z.boolean(),
    question_id: optionalString,
    limit: z.number().int().positive().nullable().optional(),
  }),
  summary: z.object({
    total_questions: z.number().int().min(0),
    supported_questions: z.number().int().min(0),
    unsupported_questions: z.number().int().min(0),
    passed_questions: z.number().int().min(0),
    failed_questions_count: z.number().int().min(0),
    request_error_count: z.number().int().min(0),
    answer_readiness_accuracy: z.number().min(0).max(1),
    fallback_accuracy: z.number().min(0).max(1),
    retrieval_page_hit_rate: z.number().min(0).max(1),
    keyword_match_rate: z.number().min(0).max(1),
    average_confidence: z.number().min(0).max(1),
    average_supported_confidence: z.number().min(0).max(1),
    average_latency_ms: z.number().min(0),
    average_citation_count: z.number().min(0),
  }),
  results: z.array(evaluationCaseSchema),
});

export type ParsedEvaluationArtifact = z.infer<typeof evaluationArtifactSchema>;
export type ParsedEvaluationCase = z.infer<typeof evaluationCaseSchema>;
